# Write a Python program to find the addition and subtraction of two numbers.

# Ask the user to input two numbers (num1 and num2). 
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Given those two numbers, add them together to find the output.
addition = num1 + num2

# Subtract the two numbers to find the output.
subtraction = num1 - num2

# Print the results for the user.
print(f"The addition of {num1} and {num2} is: {addition}")
print(f"The subtraction of {num1} and {num2} is: {subtraction}")

# End of the program