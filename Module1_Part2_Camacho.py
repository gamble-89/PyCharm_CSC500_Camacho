# Write a Python program to find the multiplication and division of two numbers.

# Ask the user to input two numbers (num1 and num2). 
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Given those two numbers, multiply them together to find the output.
product = num1 * num2

# Divide the two numbers to find the output.
quotient = num1 / num2

# Print the results for the user.
print(f"The multiplication of {num1} and {num2} is: {product}")
print(f"The division of {num1} and {num2} is: {quotient}")

# End of the program